const month="April";

switch (month) {//switch statement example
    case "January":
        console.log("January");
        break;
    case "February":
        console.log("February");
        break;
    case "March":
        console.log("March");
        break;
    case "April":
        console.log("April");
        break;
    default:
        console.log("Invalid month");
        break;
}